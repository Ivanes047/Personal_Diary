    private void saveMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveMenuItemActionPerformed
        try {
            LocalDate localDate = LocalDate.now();
            File file = new File("./Дневник/"+ DateTimeFormatter.ofPattern("dd_MM_yyyy").format(localDate)+".txt");
            if(file.exists()){
                file.delete();
            }
            Writer filWriteStream = new FileWriter(new File(".").getAbsolutePath()+"//Дневник//" + DateTimeFormatter.ofPattern("dd_MM_yyyy").format(localDate) + ".txt",true);
            filWriteStream.write(jTextArea1.getText());
            filWriteStream.close();
        } catch (IOException e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Ошибка записи в файл \n" + file.getAbsolutePath());
        }    
    }//GEN-LAST:event_saveMenuItemActionPerformed

