    private void saveASMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveASMenuItem1ActionPerformed
        //fileChooser.setFileFilter(fileFilter);
        if (fileChooser.showSaveDialog(null) != JFileChooser.APPROVE_OPTION) {
            return;//Нажали Cancel
        }
        file = fileChooser.getSelectedFile();
        try {
            Writer filWriteStream = new FileWriter(file);
            filWriteStream.write(jTextArea1.getText());
            filWriteStream.close();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Ошибка записи в файл \n" + file.getAbsolutePath());
        }   
    }//GEN-LAST:event_saveASMenuItem1ActionPerformed

