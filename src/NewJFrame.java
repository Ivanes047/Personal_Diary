    private void openMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openMenuItem1ActionPerformed
        fileChooser.setCurrentDirectory(new File("."));
        fileChooser.addChoosableFileFilter(fileFilterTxt);
        if (fileChooser.showOpenDialog(null) != JFileChooser.APPROVE_OPTION) {
            return;//Нажали Cancel
        }
        file = fileChooser.getSelectedFile();
        try {
            FileReader filReadStream = new FileReader(file);
            BufferedReader bufferedIn = new BufferedReader(filReadStream);
            String s="",tmpS="";
            while((tmpS = bufferedIn.readLine())!=null)
            s+=tmpS+"\n";
            jTextArea1.setText(s);
            bufferedIn.close();
        } catch (IOException e) {
            javax.swing.JOptionPane.showMessageDialog(null,
                    "Ошибка чтения из файла \n" + file.getAbsolutePath());
        }
        
    }//GEN-LAST:event_openMenuItem1ActionPerformed

